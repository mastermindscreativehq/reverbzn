function generateTrackPlatformMetrics(
  trackId: string,
  platformSlug: string,
  basePlays: number,
  baseRevenue: number,
  weeksBack = 12
): PlatformTrackMetric[] {
  return Array.from({ length: weeksBack }, (_, i) => ({
    id: `metric_${trackId}_${platformSlug}_${i}`,
    trackId,
    platformSlug: platformSlug as never,
    date: format(subDays(new Date(), (weeksBack - 1 - i) * 7), "yyyy-MM-dd"),
    plays: Math.floor(basePlays * (0.7 + 0.3 * (i / weeksBack)) * (0.85 + Math.random() * 0.3)),
    saves: Math.floor(basePlays * 0.12 * (0.85 + Math.random() * 0.3)),
    listeners: Math.floor(basePlays * 0.78 * (0.85 + Math.random() * 0.3)),
    streams: Math.floor(basePlays * (0.7 + 0.3 * (i / weeksBack)) * (0.85 + Math.random() * 0.3)),
    revenueEstimateUsd: parseFloat((baseRevenue * (0.85 + Math.random() * 0.3)).toFixed(2)),
  }));
}

