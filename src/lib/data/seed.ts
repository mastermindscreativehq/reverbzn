// WEEKLY TREND DATA (last 12 weeks)
// ============================================================
export function generateWeeklyTrends(baseValue: number, growthRate: number, weeks = 12): TrendPoint[] {
  return Array.from({ length: weeks }, (_, i) => {
    const weekOffset = weeks - 1 - i;
    const noise = 0.85 + Math.random() * 0.3;
    const value = Math.floor(baseValue * Math.pow(1 + growthRate, i) * noise);
    return {
      date: format(subDays(new Date(), weekOffset * 7), "MMM d"),
      value,
    };
  });
}

export function generateDailyTrends(baseValue: number, growthRate: number, days_ = 30): TrendPoint[] {
  return Array.from({ length: days_ }, (_, i) => {
    const dayOffset = days_ - 1 - i;
    const noise = 0.8 + Math.random() * 0.4;
    const value = Math.floor(baseValue * Math.pow(1 + growthRate / 30, i) * noise);
    return {
      date: format(subDays(new Date(), dayOffset), "MMM d"),
      value,
    };
  });
}

